class ExpressionsAssignment4
{
	public static void main(String args[])
	{
	    int x=5, y=10;
		System.out.println("Expressions X+Y*2 " +(x+y*2));
		System.out.println("Expressions X-Y+2 " +(x-y+2));
		System.out.println("Expressions (X+Y)*2   " + ((x+y)*2));
		System.out.println("Expressions y%x " + (y%x));
		
	}
}

